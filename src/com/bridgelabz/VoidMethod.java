package com.bridgelabz;

public class VoidMethod {
    public static void main(String[] args) {
        int a = 30;
        int b = 15;
        int sum = a + b;
        System.out.println(sum);

        int sub = a - b;
        System.out.println(sub);

        int prod = a * b;
        System.out.println(prod);

        int div = a / b;
        System.out.println(div);
    }
}

